CREATE TABLE quartier (
	id_quart INT(5) NOT NULL, 
	lib_quart VARCHAR(250) NOT NULL, 
	id_arrond INT(5) NOT NULL, 
	PRIMARY KEY (id_quart) 
	) ENGINE=InnoDB; 
